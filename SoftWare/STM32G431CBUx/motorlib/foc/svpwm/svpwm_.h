/**
 * @brief 标准七段式 SVPWM（Space Vector PWM）
 *
 * =========================== 边界条件（算法契约） ===========================
 *
 * 一、输入定义（纯数学模型）
 * ---------------------------------------------------------------------------
 * 1. v_alpha, v_beta 为 αβ 静止坐标系下的电压参考量
 *    - 与具体电机、PWM 定时器、驱动芯片无关
 *    - 不包含任何硬件物理特性
 *
 * 二、归一化基准（非常重要，唯一合法定义）
 * ---------------------------------------------------------------------------
 * 2. 输入电压必须按 SVPWM 线性区最大能力进行归一化：
 *
 *        v_alpha = U_alpha / (Vdc / sqrt(3))
 *        v_beta  = U_beta  / (Vdc / sqrt(3))
 *
 *    物理含义：
 *    - |v| = 1.0 表示 SVPWM 六边形内切圆的极限
 *    - 即 SVPWM 所能提供的最大“线性相电压峰值”
 *
 * 三、线性调制区约束
 * ---------------------------------------------------------------------------
 * 3. 本函数仅定义在线性调制区内：
 *
 *        sqrt(v_alpha^2 + v_beta^2) <= 1.0
 *
 *    - 超出该范围的行为未定义
 *    - 本算法不包含任何形式的过调制或限幅
 *
 * 四、时间约束（七段式成立条件）
 * ---------------------------------------------------------------------------
 * 4. 一个 PWM 周期被归一化为 1.0，满足：
 *
 *        T1 >= 0, T2 >= 0, T0 >= 0
 *        T1 + T2 + T0 = 1
 *
 * 5. 七段式约束：
 *
 *        T0 必须被严格对称拆分为 T0/2 + T0/2
 *
 *    对应的矢量序列为：
 *        V0 → Vx → Vy → V7 → Vy → Vx → V0
 *
 * 五、数值健壮性要求
 * ---------------------------------------------------------------------------
 * 6. 扇区边界（60° 整数倍）处允许存在浮点误差
 *    - 计算得到的 T1 / T2 / T0 在使用前必须限制在 [0.0, 1.0]
 *    - 防止 -1e-7 等数值误差导致非法占空比
 *
 * 六、零矢量奇点处理
 * ---------------------------------------------------------------------------
 * 7. 输入 (v_alpha = 0, v_beta = 0) 是合法状态
 *    - 输出必须为对称零矢量：
 *
 *          dabc = {0.5, 0.5, 0.5}
 *
 *    - 不允许出现除零、未定义扇区等异常行为
 *
 * 七、输出占空比定义（逻辑语义）
 * ---------------------------------------------------------------------------
 * 8. dabc[0..2] ∈ [0.0, 1.0]
 *
 *    逻辑定义：
 *    - duty = 1.0 ：上桥臂在整个周期内导通
 *    - duty = 0.0 ：上桥臂在整个周期内关断
 *
 * 八、显式排除项
 * ---------------------------------------------------------------------------
 * 9. 本算法不涉及：
 *    - 死区时间
 *    - PWM 对齐方式
 *    - 上下桥互补
 *    - DPWM / 不连续调制
 *    - 最小导通时间
 *
 * ===========================================================================
 */
